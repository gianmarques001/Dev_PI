    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        ArrayList<Cliente> listaCliente = ClienteDAO.listarClientes();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (Cliente cliente : listaCliente) {
            System.out.println("id " + cliente.getIdCliente());
            model.addRow(new String[]{
                String.valueOf(cliente.getIdCliente()),
                String.valueOf(cliente.getNome()),

            });
        }

    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        String codCliente = txtCodCliente.getText();

        if (codCliente.trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Campo em branco. Por favor informe o código do cliente.");
        } else {

            int cod = Integer.parseInt(codCliente);
            boolean retorno = ClienteDAO.excluirCliente(cod);

            if (retorno) {
                JOptionPane.showMessageDialog(rootPane, "Sucesso!");
                
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falha!");
            }

        }
    }//GEN-LAST:event_btnExcluirActionPerformed

